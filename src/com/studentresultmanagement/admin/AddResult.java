    private void SaveResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveResultActionPerformed
       String rollNumber = this.RollNumber.getText();
         Pattern pattern = Pattern.compile("^[1-9]\\d*$");
        Matcher matcher = pattern.matcher(rollNumber);
        boolean checkRollNumber =  matcher.matches();
        pattern = Pattern.compile("^(100(?:\\.0{1,2})?|\\d{0,2}(?:\\.\\d{1,2})?)$");
        String physics = this.physics.getText();
        matcher = pattern.matcher(physics);
        boolean sub1 = matcher.matches();
        String math = this.maths.getText();
        matcher = pattern.matcher(math);
        boolean sub2 = matcher.matches();
         String em = this.em.getText();
        matcher = pattern.matcher(em);
        boolean sub3 = matcher.matches();
         String dbms = this.dbms.getText();
        matcher = pattern.matcher(dbms);
        boolean sub4 = matcher.matches();
         String os = this.os.getText();
        matcher = pattern.matcher(os);
        boolean sub5 = matcher.matches();
        
        
        if(checkRollNumber &&sub1&&sub2&& sub3&&sub4&&sub5){
            Repository instance = Repository.getInstance();
            boolean dataSaved = instance.addResult(rollNumber,physics,math,em,dbms,os);
            if(dataSaved){
                JOptionPane.showMessageDialog(null,"Result Added Successfully");
                this.setVisible(false);
                new AddResult();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Give Proper inputs to the field");
        }
    }//GEN-LAST:event_SaveResultActionPerformed

